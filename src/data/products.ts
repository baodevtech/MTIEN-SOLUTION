/* ---- Helper functions ---- */

export function getProductBySlug(slug: string): Product | undefined {
  return PRODUCTS.find((p) => p.slug === slug);
}

export function getProductsByCategory(category: string): Product[] {
  return PRODUCTS.filter((p) => p.category === category);
}

export function getFeaturedProducts(): Product[] {
  return PRODUCTS.filter((p) => p.badge === 'Best Seller' || p.badge === 'Hot');
}

export function getFlashSaleProducts(): Product[] {
  return PRODUCTS.filter((p) => p.discount);
}

export function getRelatedProducts(slug: string, limit = 5): Product[] {
  const product = getProductBySlug(slug);
  if (!product) return PRODUCTS.slice(0, limit);
  return PRODUCTS.filter((p) => p.category === product.category && p.slug !== slug).slice(0, limit);
}
